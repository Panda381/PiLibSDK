int main()
{
	int i;

	memset(TextBuf, ' ', TEXTSIZE);

	// main loop
	while (True)
	{
		// calculate
		Calc();

		// display the number
		u32* s = &Acu[1];
		u32 n;
		Bool ok = True;
		char ch, ch2;
		int k = 2;
		int ii = LEN-2;
		if (ii > (TEXTSIZE-NUMBEG)/8) ii = (TEXTSIZE-NUMBEG)/8;
		char* b = &TextBuf[NUMBEG];
		for (i = 1; i <= ii; i++)
		{
			n = *s++;

			ch = n/10000000 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/1000000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/100000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/10000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/1000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/100) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/10) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = n % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;
		}

		*b++ = 0;

		// display result
		memcpy(TextBuf, ok ? OkTxt : ErrTxt, sizeof(OkTxt)-1);

		int x, y;
		char* ss = TextBuf;
		for (y = 0; y < TEXTHEIGHT; y++)
		{
			for (x = 0; x < TEXTWIDTH; x++)
			{
				DrawCharBg(*ss++, x*FONTW, y*FONTH, COL_WHITE);
			}
		}
		DispUpdate();

		// keyboard
		int key = KeyGet();
		if (key == KEY_ESC) Reboot();	// Program exit
		if (key == KEY_SCREENSHOT) ScreenShot(); //  Screenshot - This may take a few seconds to write.
	}

	return 0;
}
