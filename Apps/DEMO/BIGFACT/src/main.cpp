const char OkTxt[]   = "   Result OK";
const char ErrTxt[]  = "   > ERROR <";

real8192 Num;

int main()
{
	// prompt
	DrawPrint("Calculating... (wait up to 50 seconds)");
	DispUpdate();

	// enter input number to calculate
	Real8192SetUInt(&Num, NUMBER);

	// calculate factorial Log10
	Real8192FactLog(&Num);

	// get exponent
	int exp = Real8192GetUInt(&Num);

	// remove integer part
	Real8192TruncFrac(&Num);

	// convert to decimal number
	Real8192Exp10(&Num);

	// print mantissa to text buffer
	Real8192ToText(&Num, TextBuf, DIGITS+1);

	// display result
	DrawPrint("\f\27%d! = %se+%d", NUMBER, TextBuf, exp);

	// check result
	DrawPrint((memcmp(TextBuf, Sample, DIGITS+2) == 0) ? OkTxt : ErrTxt);
	DispUpdate();

	// main loop
	while (True)
	{
		// keyboard
		int key = KeyGet();
		if (key == KEY_ESC) Reboot();	// Program exit
		if (key == KEY_SCREENSHOT) ScreenShot(); //  Screenshot - This may take a few seconds to write.
	}

	return 0;
}
